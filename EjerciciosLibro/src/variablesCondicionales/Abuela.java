package variablesCondicionales;

public class Abuela {

	int m;
	int n;
	@Override
	public String toString() {
		return "Abuela [m=" + m + ", n=" + n + "]";
	}
	public int getM() {
		return m;
	}
	public void setM(int m) {
		this.m = m;
	}
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	
	
}
